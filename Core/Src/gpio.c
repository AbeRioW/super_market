void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
		if(GPIO_Pin==KEY_DOWN_Pin)
		{
			  button = DOWN_BUTTON;
		}
		
		if(GPIO_Pin==KEY_UP_Pin)
		{
				button = UP_BUTTON;
		}
		
		if(GPIO_Pin==KEY_ENSURE_Pin)
		{
				button  = ENSURE_BUTTON;
		}
}


